    private void btnSigInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSigInActionPerformed
        String name = txtName.getText();
        String user = txtUser.getText();
        String password = txtPassword.getText();
        
        if(name.isEmpty()||user.isEmpty()||password.isEmpty()){
            JOptionPane.showMessageDialog(null, "No has llenado todas las casillas!");
        }else{
            if(instanciaMain.agregarUsuario(name, user, password)){
                JOptionPane.showMessageDialog(null, "Se ha registrado correctamente!");
                if(instanciaMain.admin[1]==null){
                    JOptionPane.showMessageDialog(null, "Crea otra cuenta");
                    txtName.setText("");
                    txtUser.setText("");
                    txtPassword.setText("");
                }else{
                    new Oponente(instanciaMain).setVisible(true);
                }
            }else{
                JOptionPane.showMessageDialog(null, "ERROR!");
            }
        }
    }//GEN-LAST:event_btnSigInActionPerformed

